            <h4>The Final Result</h4>
            </div>
            <div class="col-xs-8 col-xs-offset-2" id="fixedEssay">

<p>Last summer, my dad took my on a camping trip. I had never camped before and my dad thought it would be good for us to bond, away from civilization. We packed and headed out not for a campground with tent sites and shower rooms, but for the back regions of swamps and raspberry bushes, at least a thousand miles from home and regular communication.</p>
<p>We had to walk through a pond to where we would set up our home-away-from home. It was a terrible trek, and when we had finally arrived, I was already set to leave. But, no. We had to unpack our gear, prepare the ground, put up the tent, and then think about food. My dad suggested that we try to catch some grub from the stream, so we got our fishing gear ready and waded in the cold, leech-infested water. Yuck. It was a good 45 minutes later, while the sun set and the flies bit, that we got our first bites. I was able to get two trout, and dad finished off with two more. We gutted them and fried them, and they turned out to be delicious. The next two weeks were going to be difficult. But, all this time out in the wild gave me time to bond with my dad.</p>
<p>He and I had never really talked like we did over those two weeks. It’s amazing how many things had been left unsaid over the years after he divorced my mom. He told me about how much the divorce hurt, how he and mom had met and fell in love, and how much he loved me. I asked him about why he and mom divorced, and how he felt about my mom now. He explained it all. The divorce didn’t happen out of no-where; there had been problems even before I was born.  They also didn't hate me or each other. They had good and bad feelings and memories, just like I did. I began to see my dad and my mom through different eyes. I saw them as people, separate from myself, who have their own emotions and their own struggles. It was a revelation - an adult one - that it wasn’t all about me and that nobody else, including my dad, lived a perfect life. He was human, just like me. </p>
<p>When dad and I left the woods, we were still sweating and the deer flies were still biting, but I felt different. I felt older. Sometimes we reduce other people's lives to a magazine with the best appliances and the nicest furniture. But that's not what it really is, and my dad helped me realize that. I'm glad I spent two weeks in the harsh wilderness with him.</p>

