                <p>1. One glaring problem you have with your essay is that it is over the word limit by nearly 200 words. 
                What I suggest you do to cut this down is to focus on exactly one topic. Currently, you focus on 
                your realization that everyone has their own lives, and that not everything is 
                as perfect as it seems, but you also focus on how you learned that leaving everything behind 
                on this camping trip wasn't so bad. You are juggling two topics, so your answer to the prompt is unclear; I suggest you focus only the first topic, and focus on how your realization was sparked by
                bonding with your dad.
                <p>2. The first paragraph states exactly what your essay proceeds to show. I would consider removing 
                the first paragraph entirely, or replacing it with a single sentence saying that your father 
                wanted to take you camping.
                <p>3. The second and third paragraph can be combined into one paragraph. I would shorten the descriptions 
                in the third paragraph significantly since they do little to develop the focus of the essay.
                <p>4. You should use the fourth paragraph to talk about how doing these camping tasks gave you time to 
                bond with your father. Your fifth paragraph should then elaborate on how you bonded.
                <p>5. The realization that not every has their own life that doesn't revolve around you, and that their lives 
                aren't as perfect as you'd imagined is the very core idea of this essay. It is very important that 
                you end your essay with something relating to this idea, since it directly answers the prompt.
                <p>6. With the above thought in mind, I think the last paragraph is currently pretty messy. The magazine analogy 
                is strange and underdeveloped, and you try to juggle the the two topics I mentioned in the first comment, with the sentence starting with "That strength". Talk instead about how you felt you had grown from this camping trip, and perhaps try to come up with a different metaphor for what you learned. Also, don't end the essay with "But, that's not all bad." It leaves the essay sounding unfinished.
                <p>7. On a smaller note, you tend to use different words to describe your dad, such as "Dad", "my dad", and "father". I suggest you stick to one thing throughout your essay.

